$mediaFiles = [];
$directory = __DIR__;

// Supported formats
$supportedFormats = ['m4a', 'mp4', 'mp3', 'wav', 'ogg', 'webm'];

// Scan directory
$files = scandir($directory);
foreach ($files as $file) {
    $extension = strtolower(pathinfo($file, PATHINFO_EXTENSION));
    if (in_array($extension, $supportedFormats)) {
        // NOTEBOOKLM FIX: Treat MP4 as audio, only WebM is video
        $type = in_array($extension, ['webm']) ? 'video' : 'audio';
        
        $mediaFiles[] = [
            'filename' => $file,
            'extension' => $extension,
            'type' => $type,
            'modified' => filemtime($file),
            'size' => filesize($file)
        ];
    }
}

// Default Sort: Modification date (newest first)
usort($mediaFiles, function($a, $b) {
    return $b['modified'] - $a['modified'];
});

// Helper function to format bytes
function formatBytes($bytes, $precision = 1) { 
    $units = array('B', 'KB', 'MB', 'GB', 'TB'); 
    $bytes = max($bytes, 0); 
    $pow = floor(($bytes ? log($bytes) : 0) / log(1024)); 
    $pow = min($pow, count($units) - 1); 
    $bytes /= pow(1024, $pow);
    return round($bytes, $precision) . ' ' . $units[$pow]; 
}

// Calculate total library size
$totalSize = array_sum(array_column($mediaFiles, 'size'));
$totalSizeFormatted = formatBytes($totalSize);

