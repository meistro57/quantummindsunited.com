    searchInput.addEventListener('input', (e) => {
        const term = e.target.value.toLowerCase().trim();
        const cards = document.querySelectorAll('.track-item');
        let visibleCount = 0;
        let latestTimestamp = 0;

        cards.forEach(card => {
            const title = card.dataset.title.toLowerCase();
            if(title.includes(term)) {
                card.style.display = 'flex';
                visibleCount++;
                
                // Track latest visible file
                const modified = parseInt(card.dataset.modified);
                if(modified > latestTimestamp) {
                    latestTimestamp = modified;
                }
            } else {
                card.style.display = 'none';
            }
        });
        
        // Update stats dynamically
        trackCountPill.textContent = `üíø ${visibleCount} Track${visibleCount !== 1 ? 's' : ''}`;
        
        if(latestTimestamp > 0) {
            const latestDate = new Date(latestTimestamp * 1000);
            const formatted = latestDate.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
            latestDatePill.textContent = `üìÖ Latest: ${formatted}`;
        }
