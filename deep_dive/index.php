        }
    }

    function playPrevTrack() {
        const visibleCards = getVisibleCards();
        let currentIndex = visibleCards.indexOf(currentCard);
        
        if (currentIndex > 0) {
            const prevBtn = visibleCards[currentIndex - 1].querySelector('.play-btn-large');
            loadTrack(prevBtn);
        }
    }

