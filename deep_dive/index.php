    function playNextTrack() {
        const visibleCards = getVisibleCards();
        let currentIndex = visibleCards.indexOf(currentCard);
        
        if (currentIndex !== -1 && currentIndex < visibleCards.length - 1) {
            const nextBtn = visibleCards[currentIndex + 1].querySelector('.play-btn-large');
            loadTrack(nextBtn);
