        const visibleCards = getVisibleCards();
        if(visibleCards.length === 0) return;
        
        const randomCard = visibleCards[Math.floor(Math.random() * visibleCards.length)];
        const btn = randomCard.querySelector('.play-btn-large');
        loadTrack(btn);
        
