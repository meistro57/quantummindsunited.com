    // ==========================================
    // KEYBOARD SHORTCUTS
    // ==========================================
    document.addEventListener('keydown', (e) => {
        // Ignore if typing in search
        if(e.target === searchInput) return;
        
        // Space: Play/Pause
        if(e.code === 'Space') {
            e.preventDefault();
            if(currentButton) {
                currentButton.click();
            }
        }
        
        // Ctrl/Cmd + Right Arrow: Next Track
        if(e.code === 'ArrowRight' && (e.ctrlKey || e.metaKey)) {
            e.preventDefault();
            playNextTrack();
        }
        
        // Ctrl/Cmd + Left Arrow: Previous Track
        if(e.code === 'ArrowLeft' && (e.ctrlKey || e.metaKey)) {
            e.preventDefault();
            playPrevTrack();
        }
        
        // Escape: Close Player
        if(e.code === 'Escape') {
            closePlayer();
        }
        
        // S: Focus Search
        if(e.code === 'KeyS' && !e.ctrlKey && !e.metaKey) {
            e.preventDefault();
            searchInput.focus();
        }
    });

